package com.model;

public class Item {
	//Attributes
	private int ID;
	private String name;
	
	// getters & setters
	public int getID() {
		return ID;
	}
	public String getName() {
		return name;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public void setName(String name) {
		this.name = name;
	}
}
